#include "productdelegate.hpp"

ProductDelegate::ProductDelegate(const QList<QPair<QString, int>>& List, QWidget* Parent)
: QItemDelegate(Parent), Items(List) {}

ProductDelegate::~ProductDelegate(void) {}

QWidget* ProductDelegate::createEditor(QWidget* Parent, const QStyleOptionViewItem& Option, const QModelIndex& Index) const
{
	QComboBox* Spin = new QComboBox(Parent);

	for (const auto& Ref : Items) Spin->addItem(Ref.first, Ref.second);

	return Spin;
}
